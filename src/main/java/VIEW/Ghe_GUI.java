	private void loadGheToTable() {
	    Ghe_GIANG_DAO gheDAO = new Ghe_GIANG_DAO();
	    List<Ghe> listGhe = gheDAO.getDanhSachTatCaGhe();

	    DefaultTableModel model = (DefaultTableModel) tbGhe.getModel();
	    model.setRowCount(0); // X√≥a d·ªØ li·ªáu c≈©

	    for (Ghe ghe : listGhe) {
	        Object[] row = {
	            ghe.getMaGhe(),
	            ghe.getPhongChieu().getMaPhong(), // ho·∫∑c getMaPhong n·∫øu mu·ªën
	            ghe.getSoGhe(),
	            ghe.getLoaiGhe().toString(),
	        };
	        model.addRow(row);
	    }
	}
	private boolean validData(String maGhe, String phongChieu, String soGhe, LoaiGhe loaiGhe) {
	    if (maGhe == null || !maGhe.matches("^G\\d{3}$")) {
	        JOptionPane.showMessageDialog(null, "M√£ gh·∫ø ph·∫£i theo m·∫´u: G + 3 ch·ªØ s·ªë (VD: G001).");
	        return false;
	    }

	    if (phongChieu == null || !phongChieu.matches("^PC\\d{3}$")) {
	        JOptionPane.showMessageDialog(null, "Ph√≤ng chi·∫øu ph·∫£i theo m·∫´u: PC + 3 ch·ªØ s·ªë (VD: PC001).");
	        return false;
	    }

	    if (soGhe == null || !soGhe.matches("^[A-Z]\\d{1,2}$")) {
	        JOptionPane.showMessageDialog(null, "S·ªë gh·∫ø ph·∫£i theo m·∫´u: ch·ªØ c√°i (A-Z) + s·ªë (VD: A1, B4, C12).");
	        return false;
	    }

	    if (loaiGhe == null) {
	        JOptionPane.showMessageDialog(null, "Vui l√≤ng ch·ªçn lo·∫°i gh·∫ø.");
	        return false;
	    }

	    return true;
	}

